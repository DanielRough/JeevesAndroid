public class FirebaseUI {

    public String id;
    public String name;
    public String text;
    public String type;
    public long xPos;
    public long yPos;
    public String description;

    public String getid() {
        return id;
    }

    public String getname() {
        return name;
    }

    public String gettext() {
        return text;
    }

    public String gettype() {
        return type;
    }

    public long getxPos() {
        return xPos;
    }

    public long getyPos() {
        return yPos;
    }

    public String getdescription(){ return description; }

}
